package s01_basic.ex03_variable;

public class Example_02_Decimals_float_vs_double {
    public static void main(String[] args) {
        float smallPaint = 3.14f;    // need 'f' after the number
        double bigPaint = 3.14159265359;
        System.out.println(smallPaint);
        System.out.println(bigPaint);

    }
}
