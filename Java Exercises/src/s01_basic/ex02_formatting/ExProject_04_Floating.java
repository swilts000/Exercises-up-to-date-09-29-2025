package s01_basic.ex02_formatting;

public class ExProject_04_Floating {
    public static void main(String[] args) {
        System.out.printf("Number 3.14: %f%n", 3.14);
        System.out.printf("Number 0.0: %f%n", 0.0);
        System.out.printf("Number -2.5: %f%n", -2.5);
        System.out.printf("Number 45.46: %f%n", 45.46);
        System.out.printf("Number 100.0: %f%n", 100.0);
    }
}
