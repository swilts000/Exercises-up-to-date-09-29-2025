package s01_basic.ex02_formatting;

public class ExProject_03_Integer {
    public static void main(String[] args) {
        System.out.printf("Number 10: %n%d", 10);
        System.out.printf("%nNumber 0: %n%d", 0);
        System.out.printf("%nNumber -5: %n%d", -5);
        System.out.printf("%nNumber 1000: %n%d", 1000);
        System.out.printf("%nNumber 42: %n%d", 42);
    }
}
