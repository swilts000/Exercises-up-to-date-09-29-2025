package s01_basic.ex02_formatting;

public class ExProject_02_Character {
    public static void main(String[] args) {
        System.out.printf("Letter A: %n%c", 'A');
        System.out.printf("%nLetter z: %n%c", (char)57);
        System.out.printf("%nDigit 5: %n%c", '5');
        System.out.printf("%nSymbol #: %n%c", '#');
        System.out.printf("%nSpace: %n%c", ' ');
    }
}
