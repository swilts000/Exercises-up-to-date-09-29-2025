package s01_basic.ex02_formatting;

public class ExProject_01_Boolean {
    public static void main(String[] args) {
        System.out.printf("Boolean true: %n%b", true);
        System.out.printf("%nBoolean false: %n%b", false);
        System.out.printf("%n5 > 3: %n%b", 5 > 3);
        System.out.printf("%n10 == 20: %n%b", 10 == 20);
        System.out.printf("%n0 != 0: %n%b", 0 != 0);

    }
}
//Boolean true: true
//Boolean false: false
//        5 > 3: true
//        10 == 20: false
//        0 != 0: false
