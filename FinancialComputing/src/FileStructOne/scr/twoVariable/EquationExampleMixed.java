package FileStructOne.scr.twoVariable;

public class EquationExampleMixed {
    public static void main(String[] args) {
        double assets = 75.75; //4 Double type
        int liabilities = 25; //5 Integer type
        double equity = assets - liabilities; //6 Calculation with double
        System.out.println("Assets = Liabilities + Equity"); //7 Print equation
        System.out.println(assets + " = " + liabilities + " + " + equity); //8 Print numbers
    }
}
