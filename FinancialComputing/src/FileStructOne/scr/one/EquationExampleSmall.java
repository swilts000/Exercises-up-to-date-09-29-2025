package FileStructOne.scr.one;

public class EquationExampleSmall {
    public static void main(String[] args) {
        float assets = 50.5f; //4 Float type
        short liabilities = 20; //5 Short type
        float equity = assets - liabilities; //6 Calculation with float
        System.out.println("Assets = Liabilities + Equity"); //7 Print equation
        System.out.println(assets + " = " + liabilities + " + " + equity); //8 Print numbers
    }
}
