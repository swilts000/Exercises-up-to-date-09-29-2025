package fc18_myStrings_methods;

public class Ex01Length {
    public static void main(String[] args) {
        // 1. Declare a String variable called 'text' and assign a value
        String text = "Hello World";

        // 2. Use the length() method to find the number of characters in the string
        int len = text.length();

        // 3. Print the result with a message
        System.out.println("Length: " + len);  // Output: Length: 11
    }
}
